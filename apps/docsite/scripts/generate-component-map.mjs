/**
 * Generates apps/docsite/src/generated/xdsComponentMap.ts
 *
 * Maps every @xds/core component name to a React.lazy() wrapper that
 * dynamically imports only `@xds/core/<Name>`, so the Playground tab loads just
 * the previewed component instead of the whole barrel (issue #2019). Runs as
 * part of `pnpm generate` so new components are picked up automatically.
 * Mirrors the export scan in generate-scope.mjs.
 *
 * Run: node scripts/generate-component-map.mjs
 */

import {readFileSync, writeFileSync, mkdirSync} from 'node:fs';
import {dirname, resolve} from 'node:path';
import {fileURLToPath} from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const CORE_PKG = resolve(ROOT, '../../packages/core/package.json');
const OUT = resolve(ROOT, 'src/generated/xdsComponentMap.ts');

const HEADER = `// Copyright (c) Meta Platforms, Inc. and affiliates.

// @generated by scripts/generate-component-map.mjs — do not edit manually`;

// Read core package.json exports and extract PascalCase component subpaths.
// Same filter as generate-scope.mjs so the two stay in sync.
const corePkg = JSON.parse(readFileSync(CORE_PKG, 'utf-8'));
const exportKeys = Object.keys(corePkg.exports ?? {});

const SKIP =
  /\.(css|stylex)$|\/utils$|^\.\/theme|^\.\/hooks|^\.\/utils|^\.\/syntax|^\.\/docs|^\.\/groups|^\.$|^\.\/reset/;

const components = exportKeys
  .filter((k) => {
    if (SKIP.test(k)) return false;
    const name = k.replace('./', '');
    return /^[A-Z]/.test(name);
  })
  .map((k) => k.replace('./', ''))
  .sort();

const lines = [HEADER, ''];

lines.push(
  "import {createElement, lazy, Suspense, type ComponentType} from 'react';",
);
lines.push('');
lines.push('// eslint-disable-next-line @typescript-eslint/no-explicit-any');
lines.push('type AnyComponent = ComponentType<any>;');
lines.push('');
lines.push('// Prefer the XDS-prefixed export, falling back to a same-named one.');
lines.push('function pickExport(mod: Record<string, unknown>, name: string): AnyComponent {');
lines.push("  return (mod['XDS' + name] ?? mod[name]) as AnyComponent;");
lines.push('}');
lines.push('');
lines.push('// Wrap each per-component dynamic import in its own Suspense boundary');
lines.push('// so the returned component renders synchronously from any call site.');
lines.push('function lazyComponent(');
lines.push('  load: () => Promise<{default: AnyComponent}>,');
lines.push('): AnyComponent {');
lines.push('  const Lazy = lazy(load);');
lines.push('  // eslint-disable-next-line @typescript-eslint/no-explicit-any');
lines.push('  return function LazyXDSComponent(props: any) {');
lines.push('    return createElement(');
lines.push('      Suspense,');
lines.push('      {fallback: null},');
lines.push('      createElement(Lazy, props),');
lines.push('    );');
lines.push('  };');
lines.push('}');
lines.push('');
lines.push('/**');
lines.push(
  ' * Lazy component registry keyed by @xds/core subpath name (e.g. "Button").',
);
lines.push(
  ' * Each entry dynamically imports only that component on first render.',
);
lines.push(' */');
lines.push('export const xdsComponentMap: Record<string, AnyComponent> = {');
for (const name of components) {
  lines.push(
    `  ${JSON.stringify(name)}: lazyComponent(() =>`,
  );
  lines.push(
    `    import('@xds/core/${name}').then((m) => ({`,
  );
  lines.push(
    `      default: pickExport(m as Record<string, unknown>, ${JSON.stringify(name)}),`,
  );
  lines.push(`    })),`);
  lines.push(`  ),`);
}
lines.push('};');
lines.push('');

mkdirSync(dirname(OUT), {recursive: true});
writeFileSync(OUT, lines.join('\n'));
console.log(`✓ Generated ${OUT}`);
console.log(`  ${components.length} lazy component entries`);
